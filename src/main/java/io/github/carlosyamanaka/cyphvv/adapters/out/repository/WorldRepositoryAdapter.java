package io.github.carlosyamanaka.cyphvv.adapters.out.repository;

import io.github.carlosyamanaka.cyphvv.adapters.out.repository.entity.WorldEntity;
import io.github.carlosyamanaka.cyphvv.adapters.out.repository.mapper.WorldRepositoryMapper;
import io.github.carlosyamanaka.cyphvv.application.core.domain.World;
import io.github.carlosyamanaka.cyphvv.application.ports.out.WorldRepositoryPort;
import org.springframework.stereotype.Component;

@Component
public class WorldRepositoryAdapter implements WorldRepositoryPort {

    private final WorldJpaRepository worldJpaRepository;
    private final WorldRepositoryMapper mapper;

    public WorldRepositoryAdapter(WorldJpaRepository worldJpaRepository, WorldRepositoryMapper mapper) {
        this.worldJpaRepository = worldJpaRepository;
        this.mapper = mapper;
    }

    @Override
    public World save(World world) {
        // mapping a world domain object to a world entity object (required for saving to the database)
        WorldEntity entity = mapper.toEntity(world);

        // saving on database using JPA
        WorldEntity savedEntity = worldJpaRepository.save(entity);

        // mapping an entity to a domain, now with ID generated by database and returning it
        return mapper.toDomain(savedEntity);
    }
}